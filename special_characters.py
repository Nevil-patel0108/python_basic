print("hello\n world")   # \n = New Line (moves text to the next line)

print("hello\t world")   # \t = Tab Space (adds a horizontal tab)

print("hello\b world")   # \b = Backspace (removes the character before it)

print("hello\r world")   # \r = Carriage Return (moves cursor to the beginning of the line)

print("hello\\ world")   # \\ = Prints a single backslash (\)

print("hello\" world")   # \" = Prints a double quote (")

print("hello\' world")   # \' = Prints a single quote (') 
